/*
Write a program that displays the following pattern:
   *
  ***
 *****
*******
 *****
  ***
   *
 */

package com.challenges;

public class StarPattern {
    public static void main(String [] args) {

        System.out.println("   *   ");
        System.out.println("  ***  ");
        System.out.println(" ***** ");
        System.out.println("*******");
        System.out.println(" ***** ");
        System.out.println("  ***  ");
        System.out.println("   *   ");
    }
}